<p class="intro">The Day Worker Center of Mountain View is a 501(c)3 non-profit organization serving the communities surrounding Mountain View, Los Altos, and Sunnyvale. The Center was established in 1996 by leaders from local businesses, churches, and the community to provide job-matching services for hundreds of local homeowners and businesses annually.
</p> 

<p class="intro">
The Center is led by a full-time Executive Director, <a href="who_we_are.php#director">Mar&iacute;a Marroqu&iacute;n</a>, a recipient of numerous awards for her work at the Center. Organizational guidance comes from a <a href="who_we_are.php#board">Board of Directors</a> composed of community and business leaders and representatives from the day laborer community. In addition, much of the day-to-day worker-related activities at the Center is managed by <strong>the Workers' Commission</strong>, composed of 8-10 current and past users of the Center.</p>

<p class="intro">
	In addition to supporting the day laborer population, the Center contributes to our community in the following ways:</p>
    <ul class="bullet">
    	<li>provides a location for service providers, such as Community Services Agency, to reach their clients</li>
		<li>reaches out to day laborers soliciting work on the streets to inform them about the center's services</li>
		<li>provides worker-led community service</li>
		<li>monthly trash pickup on local streets</li>
		<li>donates hand-made blankets for premature infants at Lucille Packard Children's Hospital at Stanford</li>
		<li>helps coordinate and participate in community events, sponsored by Rotary Club, Run for Zimbabwe, Mountain View Educational Foundation,etc.</li>
		<li>helps senior citizens with community gardens</li>
		<li>provides English as a Second Language (ESL) classes to the workers, facilitating interactions with employers and participation in local culture</li>
		<li>serves as a critical conduit for the city agencies and services to communicate with the Latino community</li>
		<li>decreases the number of workers soliciting employment on the streets</li>
		<li>reduces complaints in the areas where workers congregate, in turn reducing the number of calls to Police Departments</li>
		<li>reduces strain on local emergency rooms by providing free weekly medical visits by the <a href="http://www.gardnerfamilyhealth.org/storage/images/Gardnerflier.pdf">Gardner Health Services</a> Mobile Medical van</li>
	</ul>
